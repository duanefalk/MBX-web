		<h2>Differences Among Superfast Ford GTs</h2>
		<p>Through White Rose, which handled design, licensing and distribution of its NASCAR models, Matchbox released 4 versions of the Ford Thunderbird stock car, but used only 3 MAN#/'s.</p>
		<p>The easiest way to distinguish them is by the casting of the nose and grill. All four have a deep crease (referred to as the 'mail slot') between the hood and bumper.</p>
		<p>The first was MAN# 212 (UMID SF0368), released in 1992 and the only one used in a regular Matchbox series. Differentiating features are:</p>
		<ol>
			<li>3 cast grill panels, two rounded above and either side of a rectangle</li>
			<li>Thunderbird is cast into the lower bumper</li>
			<li>A 'flat face' profile: the headlight wells are not recessed</li>
			<li>There is a small notch cut into the top middle of the rear bumper. It doesn/'t extend all the way to the edge of the bumper though.</li>
		</ol>
