		<div class="section">
			<h2>2) Why is it called Matchbox University?</h2>
			 <p>Many of the existing sites are called &#39;on-line databases&#39; or &#39;virtual museums&#39;, so I was looking for a different theme. Since first and foremost the site provides information the idea of a university occurred to me (complete with a Matchbox Professor!). So, like a university- there is a course catalog of &#39;lessons&#39;, a library of models (the database), and even a college store where you can see Code 2 models available (at no cost) for creating and account and other activities.</p>
		</div>
	
		<div class="section">
			<h2>3) What kind of information is available on the site?</h2>
			<p>The matchbox &#39;curriculum&#39; consists of a number of short write-ups on different topics pertinent to the hobby. It is geared more for newcomers to die-cast and Matchbox collecting, or for those who may have taken a hiatus, but recently returned to the hobby and need a quick refresher/catch-up.</p>
		</div>
		
		<div class="section">
			<h2>4) What's in the database?</h2>
			<p>There is a separate menu item that will take you to a page on the details of the MBX-U database approach and structure. Briefly though, MBX-U is built around a relational database rather than a text or excel listing. There are a variety of ways you can search for models or releases (how a model is packaged and sold). The database, for the foreseeable future, will be focused on Matchbox miniatures. Currently it covers the 1:64 vehicles such as those offered in the 1-120 series, 5-packs, etc. In the future I hope to expand to Skybusters and Convoys as well.</p>
		</div>
	
		<div class="section">
			<h2>5) Do I have to learn a new numbering method?</h2>
			<p>No! The database is structured around a unique model ID that I assigned each model, and a model/version/variation/release hierarchy. BUT all of the usual methods of cataloging and organizing Matchbox models- Charlie Mack numbers, FAB/MAN#s, model names etc.- are all still there and you can use those. If you are interested in how the models are organized though, please check out &#39;About the Database&#39; from the &#39;About the Site&#39; menu.</p>
		</div>
		
		<div class="section">
			<h2>6) My own collection, really?</h2>
			<p>Indeed. You can create and name your own collection, as well as set some user-specific options. As you search for and drill down on specific model variations you can tag them to add to your collection, or add to a wish list. Once you have a collection, your model searches will show if you have that particular item or not, and of course you can print out lists of your collection or wish list models.</p>
		</div>
		
		<div class="section">
			<h2>7) Do I <em>need</em> an account?</h2>
			<p>Again, no. You can come to the site and do searches for models and releases or read the articles without creating an account or logging in. But, if you want to set some preferences on your searches, build and update your own collection, or provide feedback/input for the site, you will need to create an account. It&#39;s very simple, and of course no financial info is needed.</p>
		</div>
		
