			<h2>Differences Among NASCAR Thunderbird Models</h2>
			<p>Through White Rose, which handled design, licensing and distribution of its NASCAR models, Matchbox released 4 versions of the Ford Thunderbird stock car, but used only 3 MAN#/'s.</p>
			<p>The easiest way to distinguish them is by the casting of the nose and grill. All four have a deep crease (referred to as the 'mail slot') between the hood and bumper.</p>
                        
                        <p>The first was MAN# 212 (UMID SF0368), released in 1992 and the only one used in a regular Matchbox series. Differentiating features are:
                        <p>1)  3 cast grill panels, two rounded above and either side of a rectangle</p>
                        <p>2)  Thunderbird is cast into the lower bumper</p>
                        <p>3)  A /'flat face/' profile- the headlight wells are not recessed</p>
                        <p>4)  There is a small notch cut into the top middle of the rear bumper. It doesn/'t extend all the way to the edge of the bumper though. </p>
 			<img src="Images/Similar_Models/MAN-212.jpg" width=340>
