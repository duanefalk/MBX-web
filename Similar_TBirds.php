                        <p>UMID SF0397 1995:</p>
                        <p>1)  The nose has no cast grill panel- it is completely smooth below the /'mail slot/'. Note that there may be grill panels painted onto the surface</p>
                        <p>2)  No Thunderbird cast on the front bumpoer, thoguh it may be painted there</p>
                        <p>3)  Still has a /'flat face/' profile, no recessed light wells</p>
                        <p>4)  Rear bumper is smooth- no notch</p>
