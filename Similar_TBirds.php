			<p></p>	
			<p>NOTE: You may come across a version that has the grill and bumper casting of SF0368, but has chromed windows, and an unpainted metal base. This was a model Matchbox started to make in anticipation of a NASCAR set. They were unable to get the licensing though and never released the models. Somehow, many copies of the #28 Havoline and #3 Goodwrench versions they had produced made their way to the public in one of the factory moves. Be aware of these (photo of #28 below), but they are not listed as production models.</p>	
