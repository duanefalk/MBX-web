                        <p>The third Matchbox model was WRP02, released for the 1996-1998 NASCAR lines, under UMID SF0470: </p>
                        <p>1)  The nose of the car has a clear indent of the light wells- it's no longer a flat face.</p>
                        <p>2)  There is no cast grill. Grill openings may be painted on the nose, typically with two bullet shapes above rectangles</p>
                        <p>3)  Again, no Thunderbird cast on the front bumpoer, thoguh it may be painted there</p>
                        <p>4)  The rear bumper has a distinct notch cut in the middle. Unlike 212 (SF0368), the notch covers the entire depth of the bumper</p>
