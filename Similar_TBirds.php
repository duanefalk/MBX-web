                        <p>The second release was MAN# 268. There were two toolings for this model- one in 1994 and one in 1995. There is a casting differences on the nose but I've left them together in UMID SF0397 (and noted in version attachments which have the cast grill and which don't).</p>
                        <p>UMID SF0397 1994:</p>
                        <p>1)  The nose has only one cast grill panel- a rectangle in the center</p>
                        <p>2)  No Thunderbird cast on the front bumper, thoguh it may be painted there</p>
                        <p>3)  Still has a 'flat face' profile, no recessed light wells, but does have a groove cut across the nose at the level of the grill panel</p>
                        <p>4)  Rear bumper is smooth- no notch</p>
