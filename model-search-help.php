		<h2>Tips on Searching Models</h2>
		<ol>
			<li>Case doesn&#39;t matter in your field entries.</li>
			<li>You&#39;ll notice that each search criterion is in a horizontal bar. To get to a particular search option (e.g. &#39;Name&#39;), simply click on that title or bar and it will open up to give you an entry box.</li>
			<li>If there is a checkbox shown along with your search option (i.e. for categories with drop-down lists), always check it, as well as selecting from the drop-down list- if it&#39;s not checked the site won&#39;t search by that option, regardless of whether you&#39;ve picked an item from the list.</li>
			<li>Under the ID section, you can only enter one type of ID for search. If you enter more than one, it will use only the one closer to the bottom for the search.</li>
			<li>If you search by a range of IDs- make sure the first ID number is less than the second, otherwise the search will fail. Also, If searching for multiple UMIDs, if you make the first number an 'LR' number and the second an 'SF', you will get a LARGE range of results!</li>
			<li>Under the General Model Information section though, you can choose one or multiple categories to search by (e.g. Vehicle type=car, AND Vehicle Make=Ford). This section always uses &#39;AND&#39; logic- it will match only those models that meet all of the criteria entered.</li>
			<li>Note that all &#39;generic&#39; models- those made up by Matchbox/Mattel are entered with a Vehicle Make of &#39;Matchbox&#39; and Country of &#39;US&#39;.</li>
			<li>Unreal models (i.e. half of the 2004 lineup or Batmobile) are listed with Vehicle Type of &#39;Fantasy&#39;.</li>
			<li>The &#39;Text on the Model&#39; section will look for any models that have the entered text on their Body (i.e. as part of their design). It doesn&#39;t include text on the base of the model.</li>
			<li>If you select &#39;Text on the Model&#39 and don&#39t get a match, try entering less text, or more prominent text. Don&#39;t use punctuation.</li>
			<li>If you make selections in both the ID and General Model Info sections, it will search based on the General Model Info only. Similarly if you select &#39;Text on the Model&#39;, it will search only by that text.</li>
			<li>Once you&#39;ve entered criteria, click on the &#39;Search&#39; button to execute the search.</li>
			<li>Clicking on the Clear button will clear out whatever has been entered/selected on the page, giving you a clean slate.</li>
			<li>
				Once you&#39;ve run a search and are on the models page, you can return to the search page in 2 ways:
				<ol>
					<li>Click on the &#39Return to Model Search&#39; menu option; this gives you a blank search page for your next search</li>
					<li>Click on the browser back button. This will leave whatever you last search info was on the search page, which may give you unexpected results if you don&#39;t notice it. If you do use the back arrow, best to hit Clear before entering your next search, unless you want to return to the same search criteria (i.e. if you searched on vehicle type and want to search again on vehicle type, hitting back arrow will leave that box checked and you can choose your next type from the dropdown).</li>
