		<p>Other differentiating features are:</p>
		<ol>
			<li>The rear door on MB174 opens; it is cast shut on MB667</li>
			<li>There is a rectangular license plate cast into the front bumper of MB667; no plate on the smooth bumper for MB174.</li>
			<li>A small sidelight is cast onto both front fenders of MB667; MB174 fenders are smooth.</li>
			<li>Metal base on MB174, plastic for MB667.</li>
