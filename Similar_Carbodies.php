		<h2>Differences Between Superfast Carbodies Taxi Models</h2>
		<p>The Carbodies Taxi FX 4 R was released in 1987 as MB174 (UMID SF0322). A handful of models were made, mostly released in Britain, Australia or ROW. An updated version called the Carbodies London Taxi was issued in 2005 as MB667 (UMID SF0714), with releases in the 1-75, Superfast and other series. Although no official blank was made, several Code 2 models were created against various of the standard versions.</p>
		<p>The base information and casting are very different:</p>
		<ol>
			<li>MB174 is called Taxi FX4R; MB667 is London taxi</li>
			<li>The base dates reflect their release years</li>
			<li>The scale for MB174 is 1:60; for MB667 it's 1:63</li>
			<li>The undercarriage casting fo MB174 is simpler, with an 'open pliers' appearance and a single exhaust pipe to the rear. Dual exhaust pipes on the newer issue.</li>
		</ol>
