			<p>MB267 (SF0396)</p>
			<ol>
				<li>No Lumina inscribed in the front flange. The bottom flange is completely flat, with no rectangular indentations</li>
				<li>There is only one groove in the front</li>
				<li>The bottom of the rear spoiler has a more rounded contour</li>
				<li>Base details differ considerable. The Matchbox logo has a squashed (left to right) look, and is located in the upper left part of the base</li>
