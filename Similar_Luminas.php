			<p>MB224 / SF0360<p>

			<p>The first Matchbox-made model to be called a Lumina was a modified version of the Buick LeSabre, used in lieu of the later MB221, which was not ready in time for release.</p>
                        <p>Differentiating points from later Luminas: </p>
			<ol>
				<li>On the front end, the grill space is very flat. Other models have a curved or triangular shape</li>
				<li>Also on the front, Lumina is inscribed, as it is with MB221 (SF0360), but the groove directly above it is interrupted with 2 thin rectangular pockets.</li>
				<li>The side of the car is sculpted with a sleeker look than the later Luminas</li>
				<li>The side windows are shaped differently. The front window is narrower and the back is longer (left to right) than other models.</li>
				<li>The base is similar to MAN221, with 4 engine pipes and the Matchbox logo printed normally (not compressed as in MB267)</li>
			</ol>
